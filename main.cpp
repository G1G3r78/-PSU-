//удаление листьев
void delLeave(tree* root, int potomok) {
    tree* YD;//указатель листа на удаление
    tree* R;//указатель поддерева

    YD = root;//начало с корня
    R = NULL;

    if ((*YD).d == potomok) {
        cout << "Нет удаления - корень\n";
        return;
    }

    //пока не дойдём до листа по значению
    while (YD != NULL && ((*YD).d != potomok)) {
        R = YD;//перевод R на поддерево, YD на след. проверке на потомка
        if (potomok < (*YD).d) {
            YD = (*YD).left; //значение листа меньше данного -> влево
        } else {
            //иначе вправо
            YD = (*YD).right;
        }
    }
    //проверка что узел является листом
    if ((*YD).left == NULL && ((*YD).right == NULL)) {
        //если если потомок (лист) слева от поддерева
        if ((*R).left == YD) {
            //то разрыв с левым потомком
            (*R).left = NULL;
        }
        else {
            //иначе с правым
            (*R).right = NULL;
        }
        delete YD; //удаление листа
    } else {
        cout << "нет удаления - " << potomok << " не является листом" << endl;
    }
}

