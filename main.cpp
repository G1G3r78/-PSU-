#include <iostream>

using namespace std;

struct tree {
    int d;
    tree *left;
    tree *right;
};

tree *tr, *pnew; //глобальные указатели корень дерева и создающиеся узлы
tree *root = NULL; // указатель корня дерева

//поиск с включением
tree *search_insert(tree *Root, int x)
{
    tree *pv = Root, *prev; //указатели для создания узла и вспом. указателя на предыдущий узел
    bool f=0;//Переменная отвечающая за
    //результат поиска(найдены совпад.элемент или нет)

    //проверка на создание корня дерева
    if (pv == NULL) {
        tree *tr;
        tr = new tree;
        (*tr).d = x;
        cout << "Вставка элемента " << x << " в дерево\n";
        (*tr).left = NULL;
        (*tr).right = NULL;
        root = tr;//установление корня дерева
        return root;
    }

    while (pv && (f==0))
    {
        prev=pv; // «адрес» элемента от которого строится дерево
        if (x == (*pv).d)
        {
            f = 1; //совпадение -> выход из цикла
        }
        else
        {
            //если добавляемое число меньше предыдущего, то влево, иначе вправо
            if (x < (*pv).d)  pv = (*pv).left;
            else pv = (*pv).right;
        }
        //Выход из цикла осуществляется, тогда когда найден свободный адрес: ссылка для //вставки нового узла
    }

    //Если совпало значение элемента со значением элемента который планируется //вставить, - то выход из функции, возврат адреса элемента с которым совпало //значение
    if(f==1) {
        cout << "Найден элемент " << x << " с адресом " << pv << " - элемент не вставлен\n";
        return pv;
    }
    //Создание нового узла
    cout << "Вставка элемента " << x << " в дерево\n";
    pnew = new tree;
    (*pnew).d=x;
    (*pnew).left=NULL;
    (*pnew).right=NULL;
    if(x<(*prev).d)
        //Присоединение к левому поддереву предка
            (*prev).left=pnew;
    else
        //присоединяем к правому поддереву предка
            (*prev).right=pnew;
    return pnew;
}

//Метод вывода значения узлов дерева в процессе обхода дерева  «прямым обходом»
void printStraight(tree *tr) //указатель на корень дерева или поддерева, обход которого производится
{
    if (!tr) return; //Если отсутствует ветка дерева, то выходим из функции
    // ЕСЛИ присутствует ветка дерева, то:
    cout << (*tr).d << endl; //Вывод данных этой ветки
    printStraight((*tr).left); //поддерево слева
    printStraight((*tr).right);//поддерево  справа
    return;
}

//Метод вывода значения узлов дерева в процессе обхода дерева  «обратным обходом»
void printReverse(tree *tr) //указатель на корень дерева или поддерева, обход которого производится
{
    if (!tr) return; //Если отсутствует ветка дерева, то выходим из функции
    // ЕСЛИ присутствует ветка дерева, то:
    printReverse((*tr).left); //поддерево слева
    printReverse((*tr).right);//поддерево  справа
    cout << (*tr).d << endl; //Вывод данных этой ветки
    return;
}

//Метод вывода значения узлов дерева в процессе обхода дерева  «симметричным обходом»
void printSym(tree *tr) {
    if (!tr) return;
    printSym((*tr).left);
    cout << (*tr).d << endl;
    printSym((*tr).right);
    return;
}

//удаление 2.1
void delRightRight(tree* root, int potomok) {
    tree* YD;// указатель удаления
    tree* R;// родитель удаляемого узла

    YD = root;
    R = NULL;

    while ((YD != NULL) && ((*YD).d != potomok)) {
        R = YD;
        if (potomok < (*YD).d)
            YD = (*YD).left;
        else
            YD = (*YD).right;
    }
    if (((*YD).right != NULL) && ((*YD).left == NULL)) {
        //связываем правого потомка с родителем
        (*R).right = (*YD).right;
        (*YD).right = NULL;
        delete YD;
    }
    //cout << (*(*R).right).d << " " << (*R).d << endl;
}

